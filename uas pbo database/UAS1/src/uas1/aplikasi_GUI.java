    private void Hapus_dataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Hapus_dataActionPerformed
        // TODO add your handling code here:
        
        if (NIM_USER.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus dari tabel.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Confirmation dialog
    int confirmation = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

    if (confirmation == JOptionPane.YES_OPTION) {
        try {
            String sql = "DELETE FROM aplikasi WHERE NIM = ?";
            Connection conn = koneksiDatabase.getKoneksi();
            PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.setString(1, NIM_USER.getText());

            int rowsAffected = pstm.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus.");
            } else {
                JOptionPane.showMessageDialog(null, "Gagal menghapus. Data tidak ditemukan.");
            }
            
            load_table();
            resetForm();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_Hapus_dataActionPerformed

    private void CariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CariActionPerformed
        // TODO add your handling code here:
        if (NIM_USER.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "NIM tidak boleh kosong untuk mencari!");
            return;
        }
        try {
            String sql = "SELECT * FROM aplikasi WHERE NIM = ?";
            Connection conn = koneksiDatabase.getKoneksi();
            PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.setString(1, NIM_USER.getText());
            
            ResultSet rs = pstm.executeQuery();
            if (rs.next()) {
                Nama_USER.setText(rs.getString("Nama"));
                NS_USER.setText(rs.getString("Nilai Sikap"));
                NT_USER.setText(rs.getString("Nilai Tugas"));
                NUTS_USER.setText(rs.getString("Nilai UTS"));
                NUAS_USER.setText(rs.getString("Nilai UAS"));
                
                JOptionPane.showMessageDialog(null, "Data ditemukan.");
                NIM_USER.setEditable(false); // Lock NIM after search
            } else {
                JOptionPane.showMessageDialog(null, "Data tidak ditemukan.");
                resetForm();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error saat mencari data: " + e.getMessage());
        }
        
    }//GEN-LAST:event_CariActionPerformed

