    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
        int barisTerpilih = tablePanen.getSelectedRow();
        
        // Cek apakah ada baris yang dipilih
        if (barisTerpilih == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus terlebih dahulu!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Konfirmasi penghapusan
        int pilihan = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        
        if (pilihan == JOptionPane.YES_OPTION) {
            // Hapus data dari list
            daftarPanen.remove(barisTerpilih);
            
            // Refresh tabel dan reset form
            refreshTabel();
            resetForm();
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
        }
    }//GEN-LAST:event_HapusActionPerformed

