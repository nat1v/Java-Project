    private void load_table() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("no");
        model.addColumn("Nama");
        model.addColumn("NIM");
        model.addColumn("Sikap");
        model.addColumn("Tugas");
        model.addColumn("UTS");
        model.addColumn("UAS");
        model.addColumn("Rerata");
        model.addColumn("Skala" );
        model.addColumn("Huruf");
        
        try {
            String sql = "SELECT * FROM aplikasi";
            Connection conn = koneksiDatabase.getKoneksi();
            Statement stm = conn.createStatement();
            ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                model.addRow(new Object[]{
                    res.getInt(1), res.getString(2), res.getString(3), res.getFloat(4),
                    res.getFloat(5), res.getFloat(6), res.getFloat(7),
                    res.getFloat(8), res.getFloat(9), res.getString(10)
                });
            }
            tableData.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
        }
    } 
     
