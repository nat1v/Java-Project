    private void muatDataAwal() {
        daftarPanen.add(new petani("Budi", 1, 15000000, 750000, 1500000, 3000000));
        daftarPanen.add(new petani("Joko", 1, 25000000, 1200000, 2000000, 4000000));
        daftarPanen.add(new petani("Budi", 2, 17500000, 800000, 1600000, 3000000));
        
        // Merefresh tampilan tabel
        refreshTabel();
    }

    private void refreshTabel() {
        
        tableModel.setRowCount(0); // Menghapus semua baris yang ada
        
        // Mengisi tabel baris per baris dari list daftarPanen
        for (petani p : daftarPanen) {
            tableModel.addRow(new Object[]{
                p.getNama(),
                p.getPanen_ke(),
                formatter.format(p.getPend_bruto()),
                formatter.format(p.getBiaya_pupukpestisida()),
                formatter.format(p.getTenagakerja()),
                formatter.format(p.getSewa()),
                formatter.format(p.getPend_net()),
                formatter.format(p.getOwner_bruto()),
                formatter.format(p.getOwner_net()),
                formatter.format(p.getPenyakap_bruto()),
                formatter.format(p.getPenyakap_net())
            });
        }
        
    }
    
